class Game:
	def start():
		
		gameExit = False

		vid = Video("C:\\Users\\Valentin\\Desktop\\Programmin\\PROJECT PY\\nodont.mp4")
		vid.set_size((WIDTH, HEIGHT))
		start_time = time.time()

		while not gameExit:
			for event in pg.event.get():
				if event.type == pg.MOUSEBUTTONDOWN:
					vid.close()
					Game.enter_tag()
				if event.type == pg.QUIT:
					gameExit = True

			vid.draw(gameDisplay, (0, 0))
			pg.display.update()

			current_time = time.time()
			elapsed_time = current_time - start_time
			if int(elapsed_time) > 2:
				vid.close()
				Game.enter_tag()
	def enter_tag():
		gameExit = False
		logged = False

		label = Label(300, 200, "Input tag:")
		input_box = InputBox(300, 250, 200, 50)

		while not gameExit:
			for event in pg.event.get():
				if event.type == pg.QUIT:
					gameExit = True

				if(input_box.handle_event(event) == 420):
					Game.main_game()

				input_box.update()

				gameDisplay.fill((30, 30, 30))

				label.draw(gameDisplay)
				input_box.draw(gameDisplay)


			pg.display.update()

			Clock = pg.time.Clock()
			Clock.tick(30)
		if gameExit:    
			pg.quit()
			quit()
	def main_game():
		gameExit = False

		newLevel = Level(matrix)
		newLevel.draw()
		shop_open = False
		build = False
		selected = None
		more = False
		is_open_attack = False
		attack = False
		startup = False

